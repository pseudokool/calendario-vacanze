This calendar is not a vacation planning calendar - it is a free-time planning calendar.
<br/>
<br/>
You can use the blessing of long weekends in a variety of ways - of course go somewhere or even plan your 'spring-cleaning', your movie-marathon, your closet makeover etc etc. Whatever is your poison - which you can't enjoy because there is no time !
<br/>
<br/>
The calendar assumes a 5 day working week. If this is not the case for you, all you have to is to unselect your working day (saturday perhaps) which has been marked as a time-off day.
<br/>
<br/>
You can choose to mark (or unmark) whatever days you want. Your children's birthdays, world cup start day, finals day, exams, performance review at work, whatever you want highlighted. To keep it simple, you can't type in what day it is but hey - take a print and write it down with a pen. Go all retro :-)
<br/><br/>
Your anniversary - so you can see when you need to take off / buy a gift / book a nice meal etc.
<br/><br/>

Once you have done all this, i want you to do two things :
<ol>
	<li>
		 Take print outs. Put one on your office table or softboard, put another one inside your cupboard at home, put another in the bathroom mirror. you get the idea. See it often. That's the key.
	</li>
	<li> 
		If there are any buddies involved in your time and activity planning - send them the calendar so they can do the same thing. Mark their key days, take print outs, stick them in 3 - 4 places.
	</li> 
	<li>
		If you like this, please forward, share, email the link to your friends who can also use it.
	</li>
</ol>
		
